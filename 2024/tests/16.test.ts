import { describe, expect, test } from '@jest/globals';
import { Maze } from '../16/maze';

describe('Day 16', () => {
  describe('Part 1', () => {
    test('should score 7036', () => {
      const lines = [
        '###############',
        '#.......#....E#',
        '#.#.###.#.###.#',
        '#.....#.#...#.#',
        '#.###.#####.#.#',
        '#.#.#.......#.#',
        '#.#.#####.###.#',
        '#...........#.#',
        '###.#.#####.#.#',
        '#...#.....#.#.#',
        '#.#.#.###.#.#.#',
        '#.....#...#.#.#',
        '#.###.#.#.#.#.#',
        '#S..#.....#...#',
        '###############',
      ];
      const maze = new Maze(lines);
      expect(maze.getScore()).toBe(7036);
    });

    test('should score 11048', () => {
      const lines = [
        '#################',
        '#...#...#...#..E#',
        '#.#.#.#.#.#.#.#.#',
        '#.#.#.#...#...#.#',
        '#.#.#.#.###.#.#.#',
        '#...#.#.#.....#.#',
        '#.#.#.#.#.#####.#',
        '#.#...#.#.#.....#',
        '#.#.#####.#.###.#',
        '#.#.#.......#...#',
        '#.#.###.#####.###',
        '#.#.#...#.....#.#',
        '#.#.#.#####.###.#',
        '#.#.#.........#.#',
        '#.#.#.#########.#',
        '#S#.............#',
        '#################',
      ];
      const maze = new Maze(lines);
      expect(maze.getScore()).toBe(11048);
    });
  });
});
